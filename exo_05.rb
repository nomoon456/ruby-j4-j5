puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #affichage de la string entre ""

puts 3 + 2 < 5 - 7 #boolean qui repondra par true ou false si 5 < -2

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #affichage de la string et le resultat de 3+2
puts "Ça fait combien 5 - 7 ? #{5 - 7}" #affichage de la string et du resultat de 5-7

puts "Ok, c'est faux alors !" #affichage de la string entre ""

puts "C'est drôle ça, faisons-en plus :" #affichage de la string entre ""

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #affichage de la string et indique si 5 > -2
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #affichage de la string et indique si 5 >= -2
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" #affichage de la string et indique si 5 <= -2
